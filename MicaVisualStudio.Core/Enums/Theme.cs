namespace MicaVisualStudio.Enums;

/// <summary>
/// Specifies the theme to use.
/// </summary>
public enum Theme
{
    /// <summary>
    /// Specifies to use the same theme as Visual Studio.
    /// </summary>
    VisualStudio,

    /// <summary>
    /// Specifies to use the same theme as the system.
    /// </summary>
    System,

    /// <summary>
    /// Specifies to use a light theme.
    /// </summary>
    Light,

    /// <summary>
    /// Specifies to use a dark theme.
    /// </summary>
    Dark
}
